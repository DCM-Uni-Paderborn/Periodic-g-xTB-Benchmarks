#!/usr/bin/env bash
set -euo pipefail

# Frozen workspace used for the archived 2026-07-14 calculations.
WS=${WS:-/home/kuehne88/work/codex-gxtb-pbc-20260714T1038Z-18d37c-1449feb}
INPUT_DIR=${INPUT_DIR:-$WS/benchmark/H2O-molecular-limit-20260714}
OUT=${OUT:-$INPUT_DIR/reproduction}
CP2K=$WS/install/cp2k/bin/cp2k.psmp

export PATH="$WS/install/save_tblite/bin:$WS/env/bin:$PATH"
export LD_LIBRARY_PATH="$WS/install/save_tblite/lib:$WS/install/save_tblite/lib64:$WS/env/lib:${LD_LIBRARY_PATH:-}"
export OMP_NUM_THREADS=1

run_case() {
  local case_name=$1
  local periodicity=$2
  local box=$3
  local template=$4
  local directory=$OUT/$case_name

  mkdir -p "$directory"
  cp "$INPUT_DIR/$template" "$directory/input.inp"
  (
    cd "$directory"
    "$CP2K" \
      --set "PROJECT=$case_name" \
      --set "REFPREFIX=${case_name}_cli" \
      --set "L=$box" \
      --set "PBC=$periodicity" \
      -i input.inp -o cp2k.out
  )
}

run_case H2O_0D NONE 20 H2O_gxtb_molecular_limit.inp
for box in 8 10 12 15 20 30 40 50 60 80 100 150 200; do
  printf -v case_name 'H2O_L%02d' "$box"
  run_case "$case_name" XYZ "$box" H2O_gxtb_molecular_limit.inp
done

run_case H2O_tight_0D NONE 20 H2O_gxtb_molecular_limit_tight.inp
for box in 40 100 200; do
  run_case "H2O_tight_L$box" XYZ "$box" H2O_gxtb_molecular_limit_tight.inp
done

run_case H2O_shift_0D NONE 40 H2O_gxtb_molecular_limit_shifted.inp
for box in 40 100 200; do
  run_case "H2O_shift_L$box" XYZ "$box" H2O_gxtb_molecular_limit_shifted.inp
done

# Independent replay of the molecular CLI command generated by CP2K's
# REFERENCE_CLI path. The archived direct.grad and direct.json are bitwise
# identical to the files produced during the CP2K-triggered CLI run.
direct=$OUT/H2O_0D/direct_cli
mkdir -p "$direct"
cp "$OUT/H2O_0D/H2O_0D_cli.gen" "$direct/water.gen"
(
  cd "$direct"
  tblite run --method gxtb --charge 0 --spin 0 \
    --acc 1.00000000E-01 --solver gvd --iterations 300 \
    --etemp 3.00000000E+02 --no-restart --input gen \
    --grad direct.grad --json direct.json water.gen > direct.log 2>&1
)
